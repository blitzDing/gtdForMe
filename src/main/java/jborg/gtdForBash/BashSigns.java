{

	public static final String gBCPX = "\033[32m";//Green bash chars Prefix.
	public static final String gBCSX = "\033[00m";//Green bash chars Suffix.
	public static final String yBCPX = "\033[33m";//Yellow bash chars Prefix.
	public static final String yBCSX = "\033[00m";//Yellow bash chars Suffix.
	public static final String rBCPX = "\033[31m";//Red bash chars Prefix.
	public static final String rBCSX = "\033[00m";//Red bash chars Suffix.
	public static final String bBCPX = "\033[34m";//Blue bash chars Prefix.
	public static final String bBCSX = "\033[00m";//Blue bash chars Suffix.	
	public static final String boldGBCPX = "\033[1;32m";//bold green bash chars Prefix.
	public static final String boldGBCSX = "\033[00m";//bold green bash chars Suffix.
	public static final String boldYBCPX = "\033[1;33m";//bold Yellow bash chars Prefix.
	public static final String boldYBCSX = "\033[00m";//bold Yellow bash chars Suffix.
	public static final String boldRBCPX = "\033[1;31m";//bold Red bash chars Prefix.
	public static final String boldRBCSX = "\033[00m";//bold Red bash chars Suffix.
	public static final String boldBBCPX = "\033[1;34m";//bold Blue bash chars Prefix.
	public static final String boldBBCSX = "\033[00m";//bold Blue bash chars Suffix.
}
